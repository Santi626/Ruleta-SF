    private static int getRandomNumberInRange(int min, int max) {
        //Devuelve un numero al azar entre el rango que se le proporciona
        if (min >= max) {
            throw new IllegalArgumentException("max must be greater than min");
        }

        Random r = new Random();
        return r.nextInt((max - min) + 1) + min;
    }

    @Override
    public void run() {
        //Juega
        try {
            while (true) {
                //Tiempo de apuestas
                habilitar();
                for (int i = 15; i > 0; i--) {
                    labelTiempo.setText("Tiempo de Apuestas: " + i);
                    Thread.sleep(1000);
                }
                labelTiempo.setText("Tiempo de Apuestas: 0");
                deshabilitar();
                Thread.sleep(3000);

                //Numero aleatorio
                Resultado resultado = Mesa.getResultado(getRandomNumberInRange(0, 36));
                taLog.append("Sistema --> Ha salido el " + resultado.getNum() + "\n");
                //Paga y vuelve a girar
                pagar(resultado);
                Thread.sleep(3000);
            }
        } catch (InterruptedException ex) {
            JOptionPane.showMessageDialog(this, "Ha ocurrido un problema con el juego");
        }
    }

